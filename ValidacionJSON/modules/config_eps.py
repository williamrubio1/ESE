def get_cups_contrato(eps_id: str) -> set:
    """Devuelve el conjunto de CUPS del contrato activo para la EPS dada."""
    return EPS_CONFIG.get(eps_id, {}).get('cups_contrato', set())


def registro_en_contrato(cups: str, eps_id: str) -> bool:
    """
    Verifica si un CUPS pertenece al contrato activo de la EPS.
    Si la EPS no tiene lista configurada, devuelve True (sin restricción).
    """
    cups_contrato = get_cups_contrato(eps_id)
    if not cups_contrato:
        return True
    return str(cups).strip() in cups_contrato
